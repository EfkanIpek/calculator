package com.efkancorp;

public class CalculatorFloat {

    public float addNumbers(float a, float b) {
        return a + b;
    }

    public float substractNumbers(float a, float b) {
        return a - b;
    }

    public float divideNumbers(float a, float b) {
        return a / b;
    }

    public float multiplyNumbers(float a, float b) {
        return a * b;
    }
}
